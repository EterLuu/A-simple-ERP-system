    private void search(String name) {

        AbstractTableModel results;

        results = new DefaultTableModel();

        if(type.equals("BANKACCOUNTS"))
                results = BankAccountsTableGenerator.searchBankAccount(name);
        if(type.equals("CUSTOMERS"))
                results = CustomersTableGenerator.searchCustomer(name);
        if(type.equals("ITEMS"))
                results = ItemsTableGenerator.searchItem(name);
        if(type.equals("USERS"))
                results = UsersTableGenerator.searchUser(name);
        if(type.equals("INVOICES"))
                results = OrdersTableGenerator.searchOrders(name);
        if(type.equals("RECEIPTS"))
            results = ReceiptsTableGenerator.searchReceipts(name);

        jTable1.setModel(results);
    }

