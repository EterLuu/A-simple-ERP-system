    private void callbackString() {
        if (prev != null) {
            if (prev instanceof InvoiceFrame || prev instanceof ReceiptFrame) {

                int row = jTable1.getSelectedRow();

                if(type.equals("BANKACCOUNTS")) {
                    ReceiptFrame p = (ReceiptFrame) prev;
                    p.customizeReceiptDetail((String) jTable1.getModel().getValueAt(row, 1));
                }
                if(type.equals("CUSTOMERS")) {
                    InvoiceFrame p = (InvoiceFrame) prev;
                    p.customizeCustomer((String) jTable1.getModel().getValueAt(row, 1));
                }
                if(type.equals("ITEMS")) {
                    InvoiceFrame p = (InvoiceFrame) prev;
                    Object[] obj = new Object[4];
                    obj[0] = jTable1.getModel().getValueAt(row, 0);
                    obj[1] = jTable1.getModel().getValueAt(row, 1);
                    obj[2] = jTable1.getModel().getValueAt(row, 2);
                    obj[3] = jTable1.getModel().getValueAt(row, 3);
                    p.customizeOrderDetail(obj);
                }
                if(type.equals("USERS")){
                    InvoiceFrame p = (InvoiceFrame) prev;
                    p.customizeUser((String) jTable1.getModel().getValueAt(row, 1));
                }
            }
        }
    }

